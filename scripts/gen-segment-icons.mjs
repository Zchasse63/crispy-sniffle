// Regenerates src/lib/segmentIcons.ts — bundles the curated Twemoji glyph for
// each gym segment as a base64 data URI, so the core UI never depends on a
// runtime icon CDN. Run: node scripts/gen-segment-icons.mjs
import { writeFileSync } from "node:fs";

// Curated per-type Twemoji glyphs (decided with Zach).
const MAP = {
  strength: "twemoji:flexed-biceps",
  crossfit: "twemoji:person-lifting-weights",
  big_box: "twemoji:department-store",
  boutique: "twemoji:sparkles",
  climbing: "twemoji:person-climbing",
  yoga_pilates: "twemoji:person-in-lotus-position",
  mma: "twemoji:boxing-glove",
  recovery: "twemoji:hot-springs",
  luxury: "twemoji:crown",
  cycling: "twemoji:person-biking",
  barre: "twemoji:ballet-shoes",
};

const entries = await Promise.all(
  Object.entries(MAP).map(async ([seg, icon]) => {
    const res = await fetch(`https://api.iconify.design/${icon}.svg`);
    if (!res.ok) throw new Error(`fetch ${icon}: ${res.status}`);
    const svg = await res.text();
    if (!svg.startsWith("<svg")) throw new Error(`not an svg: ${icon}`);
    const uri = `data:image/svg+xml;base64,${Buffer.from(svg).toString("base64")}`;
    return [seg, uri];
  }),
);

const body = entries.map(([seg, uri]) => `  ${seg}: "${uri}",`).join("\n");
const out = `// AUTOGENERATED by scripts/gen-segment-icons.mjs — do not edit by hand.
// Curated Twemoji glyph per gym type, inlined so the UI needs no icon CDN.
import type { GymSegment } from "@/lib/types/scout";

export const SEGMENT_ICON_SRC: Record<GymSegment, string> = {
${body}
};
`;
writeFileSync(new URL("../src/lib/segmentIcons.ts", import.meta.url), out);
console.log("wrote src/lib/segmentIcons.ts with", entries.length, "glyphs");
